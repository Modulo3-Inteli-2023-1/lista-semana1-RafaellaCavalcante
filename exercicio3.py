def  soma_dos_aninhados(listas): #função com parâmetro listas
    soma = 0 #soma começa com valor 0
    for lista in listas: #para cada lista em listas executa
        for item in lista: #para cada item em lista executa
            soma += item #soma se torna += item 
    return soma #retorna a soma






